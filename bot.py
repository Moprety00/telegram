@bot.callback_query_handler(func=lambda call: True)
def handle_cb(call):
    chat_id, uid, mid, data = call.message.chat.id, call.from_user.id, call.message.message_id, call.data
    try: bot.answer_callback_query(call.id)
    except: pass

    # --- Главное меню ---
    if data == "go_main":
        offline_games.pop(uid, None)
        send_new(chat_id, "✨ *Чудо Оолаха* приветствует тебя!", reply_markup=kb_main())

    # --- Оффлайн: Шаг 1 (Игроки) ---
    elif data == "off_start" or data == "off_back_players":
        offline_games[uid] = {'players': 3, 'spies': 1, 'theme': THEME_KEYS[0], 'cur': 0}
        m = types.InlineKeyboardMarkup(row_width=4)
        m.add(*[types.InlineKeyboardButton(str(i), callback_data=f"off_p_{i}") for i in range(3, 11)])
        m.add(types.InlineKeyboardButton("🔙 Назад", callback_data="go_main"))
        send_new(chat_id, "👥 *Шаг 1: Количество игроков*\nСколько человек будет играть?", reply_markup=m)

    # --- Оффлайн: Шаг 2 (Шпионы) ---
    elif data.startswith("off_p_") or data == "off_back_spies":
        if data.startswith("off_p_"):
            offline_games[uid]['players'] = int(data.split("_")[2])
        
        p_count = offline_games[uid]['players']
        m = types.InlineKeyboardMarkup(row_width=3)
        # Шпионов не может быть больше чем игроков - 1
        m.add(*[types.InlineKeyboardButton(str(i), callback_data=f"off_s_{i}") for i in range(1, min(p_count, 5))])
        m.add(types.InlineKeyboardButton("🔙 Назад", callback_data="off_back_players"))
        send_new(chat_id, f"🕵️ *Шаг 2: Количество шпионов*\nДля {p_count} игроков выбери число шпионов:", reply_markup=m)

    # --- Оффлайн: Шаг 3 (Темы) ---
    elif data.startswith("off_s_") or data == "off_back_themes":
        if data.startswith("off_s_"):
            offline_games[uid]['spies'] = int(data.split("_")[2])
        
        m = types.InlineKeyboardMarkup(row_width=1)
        for i, t in enumerate(THEME_KEYS):
            m.add(types.InlineKeyboardButton(t, callback_data=f"off_t_{i}"))
        m.add(types.InlineKeyboardButton("🔙 Назад", callback_data="off_back_spies"))
        send_new(chat_id, "📂 *Шаг 3: Тема игры*\nВыбери подходящую тему:", reply_markup=m)

    # --- Оффлайн: Генерация и раздача ---
    elif data.startswith("off_t_"):
        og = offline_games[uid]
        og['theme'] = THEME_KEYS[int(data.split("_")[2])]
        word = random.choice(THEMES[og['theme']])
        spy_indices = random.sample(range(og['players']), og['spies'])
        og['roles'] = [{'is_spy': i in spy_indices, 'word': word} for i in range(og['players'])]
        og['cur'] = 0
        off_show_turn(uid, chat_id)

    # --- Показ роли ---
    elif data == "off_show":
        og = offline_games[uid]
        role = og['roles'][og['cur']]
        if role['is_spy']:
            txt = "🕵️ *ТЫ ШПИОН!*\n\nТвоя задача — не выдать себя и узнать тему."
        else:
            txt = f"✅ *ТЫ МИРНЫЙ*\n\n📂 Тема: {og['theme']}\n🎯 Загадано: *{role['word']}*"
        
        m = types.InlineKeyboardMarkup()
        m.add(types.InlineKeyboardButton("🔒 Скрыть роль", callback_data="off_next"))
        send_new(chat_id, f"👤 *Игрок {og['cur']+1}, смотри:* \n\n{txt}", reply_markup=m)

    # --- Переход к следующему ---
    elif data == "off_next":
        offline_games[uid]['cur'] += 1
        off_show_turn(uid, chat_id)

    # --- Перезапуск оффлайн ---
    elif data == "off_restart":
        og = offline_games[uid]
        word = random.choice(THEMES[og['theme']])
        spy_indices = random.sample(range(og['players']), og['spies'])
        og['roles'] = [{'is_spy': i in spy_indices, 'word': word} for i in range(og['players'])]
        og['cur'] = 0
        off_show_turn(uid, chat_id)

    # --- Онлайн заглушка ---
    elif data == "online_mode":
        send_new(chat_id, "🌐 *Онлайн режим пока в доработке.*\nИспользуй Оффлайн для игры на одном телефоне!", reply_markup=kb_online_menu())

def off_show_turn(uid, chat_id):
    og = offline_games[uid]
    if og['cur'] < og['players']:
        m = types.InlineKeyboardMarkup()
        m.add(types.InlineKeyboardButton(f"👁️ Показать роль", callback_data="off_show"))
        send_new(chat_id, 
                 f"📱 *Игрок {og['cur']+1}*, нажми кнопку ниже, чтобы увидеть свою роль.\n"
                 f"_(Остальные — отвернитесь!)_", 
                 reply_markup=m)
    else:
        m = types.InlineKeyboardMarkup(row_width=1)
        m.add(types.InlineKeyboardButton("🔄 Начать заново (та же тема)", callback_data="off_restart"),
              types.InlineKeyboardButton("📂 Сменить настройки", callback_data="off_start"),
              types.InlineKeyboardButton("🏠 В главное меню", callback_data="go_main"))
        send_new(chat_id, 
                 "🏁 *Все роли распределены!*\n\n"
                 "1. Засекайте время (3-5 минут).\n"
                 "2. Задавайте вопросы друг другу.\n"
                 "3. Найдите шпиона!", 
                 reply_markup=m)

